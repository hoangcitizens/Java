package lab01;

public class Ex01 {
    public static void main(String[] args) {
        int a = 8, b = 4;
        System.out.println("Tổng của " + a + " và " + b + " = " + (a+b));
        System.out.println("Hiệu của " + a + " và " + b + " = " + (a-b));
        System.out.println("Tích của " + a + " và " + b + " = " + (a*b));
        System.out.println("Thương của " + a + " và " + b + " = " + (a/b));
    }
}
