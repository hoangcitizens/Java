    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        request.getRequestDispatcher("/view/vd1_lab03.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        //processRequest(request, response);
        try {
            Part filePart = request.getPart("photo");
            Part docPart = request.getPart("docfile");
            String realPath = request.getServletContext().getRealPath("images/");
            if (!Files.exists(Path.of(realPath)))
            {
                Files.createDirectory(Path.of(realPath));
            }
            String filename = Path.of(filePart.getSubmittedFileName()).getFileName().toString();
            String docfilename = Path.of(docPart.getSubmittedFileName()).getFileName().toString();
            filePart.write(realPath + "/" + filename);
            docPart.write(realPath + "/" + docfilename);
            request.setAttribute("img", filename);
            request.setAttribute("doc", docfilename);
            RequestDispatcher rd = request.getRequestDispatcher("/view/hienthivd1.jsp");
            rd.forward(request, response);
        } catch (Exception e) {
            
        }
    }

